using System;

class PrintNumbers
{
    static void Main()
    {
        Console.WriteLine(1);
        Console.WriteLine(101);
        Console.WriteLine(1001);

        Console.WriteLine();

        int a = 1;
        int b = 101;
        int c = 1001;

        Console.WriteLine(a);
        Console.WriteLine(b);
        Console.WriteLine(c);

        Console.WriteLine();

        string d = "1";//as string
        string e = "101";
        string f = "1001";

        Console.WriteLine(d);
        Console.WriteLine(e);
        Console.WriteLine(f);

        Console.WriteLine();

        Console.WriteLine("1\n101\n1001");//as string

        Console.WriteLine();
    }
}
